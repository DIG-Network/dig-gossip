//! Integration + unit tests for **CON-001: outbound connection via `connect_peer` semantics**.
//!
//! ## Traceability
//!
//! - **Spec + matrix:** [`CON-001.md`](../docs/requirements/domains/connection/specs/CON-001.md)
//! - **Normative:** [`NORMATIVE.md`](../docs/requirements/domains/connection/NORMATIVE.md)
//! - **SPEC:** [`SPEC.md`](../docs/resources/SPEC.md) §5.1
//!
//! ## Proof strategy
//!
//! **TLS / connector rows** exercise [`dig_gossip::load_ssl_cert`] and [`dig_gossip::create_native_tls_connector`]
//! (rustls equivalent when `--features rustls` only — STR-004). **Integration rows** spin up the local
//! [`common::wss_full_node`] acceptor so [`dig_gossip::GossipHandle::connect_to`] runs the full
//! `wss://` + [`Handshake`](dig_gossip::Handshake) + [`RequestPeers`](dig_gossip::RequestPeers) sequence
//! against a [`NodeType::FullNode`](dig_gossip::NodeType) responder, then assert [`AddressManager`] ingestion
//! via the [`GossipHandle::__con001_last_address_batch_for_tests`] hook.

mod common;

use std::time::{SystemTime, UNIX_EPOCH};

use dig_gossip::GossipHandle;
use dig_gossip::{
    create_native_tls_connector, load_ssl_cert, ChiaCertificate, GossipError, GossipService,
    Handshake, NodeType, PeerId, TimestampedPeerInfo,
};

/// **Row:** `test_tls_cert_load` — PEM generated by STR-005 helpers loads via upstream `load_ssl_cert`.
#[test]
fn test_tls_cert_load() {
    let dir = common::test_temp_dir();
    let (c, k) = common::generate_test_certs(dir.path());
    let loaded = load_ssl_cert(&c, &k).expect("load_ssl_cert");
    assert!(!loaded.cert_pem.is_empty());
    assert!(!loaded.key_pem.is_empty());
}

/// **Row:** `test_tls_cert_generate` — fresh identity for first-run / harness scenarios.
#[test]
fn test_tls_cert_generate() {
    let cert = ChiaCertificate::generate().expect("ChiaCertificate::generate");
    assert!(!cert.cert_pem.is_empty());
}

/// **Row:** `test_connector_creation` — CON-001 requires a [`tokio_tungstenite::Connector`] from node material.
