/// **Row:** `test_peer_connection_wrapping` — field wiring matches [`Handshake`] / outbound semantics (API-005 layout).
///
/// **Note:** Uses STR-005 [`common::mock_peer_connection`] for a real [`dig_gossip::Peer`] handle without CON-002 listener.
#[tokio::test]
async fn test_peer_connection_wrapping() {
    let hs = Handshake {
        network_id: common::test_network_id().to_string(),
        protocol_version: "0.0.37".to_string(),
        software_version: "unit-test/1".to_string(),
        server_port: 8444,
        node_type: NodeType::FullNode,
        capabilities: vec![(1, "x".to_string())],
    };
    let mut base = common::mock_peer_connection(true).await;
    base.node_type = hs.node_type;
    base.protocol_version = hs.protocol_version.clone();
    base.software_version = hs.software_version.clone();
    base.peer_server_port = hs.server_port;
    base.capabilities = hs.capabilities.clone();
    assert!(base.is_outbound);
    let _: dig_gossip::Peer = base.peer;
    let _: tokio::sync::mpsc::Receiver<dig_gossip::Message> = base.inbound_rx;
}

/// **Row:** `test_creation_time_set` — [`PeerConnection::creation_time`] is Unix seconds near “now”.
#[tokio::test]
async fn test_creation_time_set() {
    let before = SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .unwrap()
        .as_secs();
    let pc = common::mock_peer_connection(true).await;
    let after = SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .unwrap()
        .as_secs();
    assert!(
        pc.creation_time >= before.saturating_sub(1) && pc.creation_time <= after.saturating_add(1),
        "creation_time {} not near [{before},{after}]",
        pc.creation_time
    );
}

async fn running_client() -> (tempfile::TempDir, GossipService, GossipHandle) {
    let dir = common::test_temp_dir();
    let _ = common::generate_test_certs(dir.path());
    let cfg = common::test_gossip_config(dir.path());
    let svc = GossipService::new(cfg).expect("GossipService::new");
    let h = svc.start().await.expect("start");
    (dir, svc, h)
}

/// **Row:** `test_outbound_connect_handshake` — TLS client completes Chia handshake with matching `network_id`.
#[tokio::test]
async fn test_outbound_connect_handshake() {
    let (_cdir, _svc, h) = running_client().await;
    let server_dir = common::test_temp_dir();
    let (sc, sk) = common::generate_test_certs(server_dir.path());
    let server_cert = load_ssl_cert(&sc, &sk).expect("server tls");
    let net = common::test_network_id().to_string();
    let (addr, jh) =
        common::wss_full_node::spawn_one_shot_full_node(server_cert, net.clone(), vec![]).await;
    let pid = h.connect_to(addr).await.expect("outbound connect");
    assert_ne!(pid, PeerId::default());
    let batch = h.__con001_last_address_batch_for_tests();
    assert!(
        batch.is_some(),
        "add_to_new_table should run after RequestPeers"
    );
    jh.await.expect("join").expect("server task");
}

/// **Row:** `test_request_peers_after_connect` — `RespondPeers.peer_list` reaches [`AddressManager`].
#[tokio::test]
async fn test_request_peers_after_connect() {
    let (_cdir, _svc, h) = running_client().await;
    let server_dir = common::test_temp_dir();
    let (sc, sk) = common::generate_test_certs(server_dir.path());
    let server_cert = load_ssl_cert(&sc, &sk).expect("server tls");
    let net = common::test_network_id().to_string();
    let gossiped = TimestampedPeerInfo::new("198.51.100.42".into(), 9444, 1_700_000_000);
    let (addr, jh) =
        common::wss_full_node::spawn_one_shot_full_node(server_cert, net, vec![gossiped.clone()])
            .await;
    h.connect_to(addr).await.expect("connect");
    let (_, src) = h
        .__con001_last_address_batch_for_tests()
        .expect("batch recorded");
    assert_eq!(src.host, addr.ip().to_string());
    assert_eq!(src.port, addr.port());
    let (list, _) = h.__con001_last_address_batch_for_tests().unwrap();
    assert_eq!(list.len(), 1);
    assert_eq!(list[0].host, gossiped.host);
    assert_eq!(list[0].port, gossiped.port);
    jh.await.expect("join").expect("server");
}

/// **Row:** `test_outbound_connect_failure` — unreachable WSS peer surfaces [`GossipError::ClientError`].
#[tokio::test]
async fn test_outbound_connect_failure() {
    let (_cdir, _svc, h) = running_client().await;
    let dead: std::net::SocketAddr = "127.0.0.1:2".parse().unwrap();
    let err = h.connect_to(dead).await.unwrap_err();
    assert!(
        matches!(err, GossipError::ClientError(_)),
        "expected ClientError, got {err:?}"
    );
}
